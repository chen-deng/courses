def value(n, B_, MAX_AMT_):
  if n >= MAX_AMT_:
    return n

  vn = 0
  for i, b in enumerate(B_):
    if b == 0:
      vn += value(n + i + 1, B_, MAX_AMT_)
  return max(n, vn / float(len(B_)))

